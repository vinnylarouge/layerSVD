function renderChainsPanel() {
  const panel = document.getElementById("chains-panel");
  panel.innerHTML = "";

  if (STATE.chains.length === 0) {
    panel.innerHTML = `
      <div class="empty-msg">
        click a heatmap cell to pin a concept and start a chain
      </div>`;
    return;
  }

  for (const chain of STATE.chains) {
    const card = document.createElement("div");
    card.className = "chain-card" + (chain.id === STATE.activeChainId ? " active" : "");
    card.style.borderLeftColor = chain.color;
    card.dataset.chainId = chain.id;

    // Header
    const head = document.createElement("div");
    head.className = "chain-head";
    head.innerHTML = `
      <span class="chain-dot" style="display:inline-block;width:10px;height:10px;border-radius:50%;background:${chain.color};"></span>
      <input class="chain-label" placeholder="label this motif…" value="${escapeHtml(chain.label)}">
      <button class="chain-del" title="delete chain">×</button>
    `;
    head.querySelector(".chain-label").addEventListener("input", (e) => {
      chain.label = e.target.value;
    });
    head.querySelector(".chain-del").addEventListener("click", (e) => {
      e.stopPropagation();
      deleteChain(chain.id);
    });
    head.addEventListener("click", () => setActiveChain(chain.id));
    card.appendChild(head);

    // Nodes
    for (let ni = 0; ni < chain.nodes.length; ni++) {
      const n = chain.nodes[ni];
      const row = document.createElement("div");
      row.className = "chain-node";
      const tokens = topTokensForConcept(n.layer, n.channel, n.slot, 4);
      const chLetter = CHANNEL_LETTER[n.channel];
      // Edge weight to parent
      let edge = "";
      if (ni > 0) {
        const parent = chain.nodes[ni - 1];
        const a = activationSeries(parent.layer, parent.channel, parent.slot);
        const b = activationSeries(n.layer, n.channel, n.slot);
        if (a && b) {
          const c = cosSim(a, b);
          edge = `<span class="chain-node-edge">${c >= 0 ? "↓" : "↯"} ${c.toFixed(2)}</span>`;
        }
      }
      row.innerHTML = `
        <div class="chain-node-id">
          <span>L${n.layer.toString().padStart(2, "0")}</span>
          <span class="ch ${chLetter}">${chLetter}</span>
          <span>i=${n.slot.toString().padStart(2, "0")}</span>
          ${edge}
          <button class="node-del" title="remove this node">×</button>
        </div>
        <div class="chain-node-tokens">
          ${tokens.map((t) => `<span class="tok">${escapeHtml(t.label)}<em>${t.activation >= 0 ? "+" : ""}${t.activation.toFixed(1)}</em></span>`).join("")}
        </div>`;
      row.addEventListener("click", () => {
        STATE.activeChainId = chain.id;
        if (n.channel !== STATE.channel) {
          STATE.channel = n.channel;
          document.getElementById("channel-sel").value = n.channel;
        }
        renderHeatmap();
        renderChainsPanel();
      });
      row.querySelector(".node-del").addEventListener("click", (e) => {
        e.stopPropagation();
        removeNodeAt(chain.id, ni);
      });
      card.appendChild(row);
    }

    // Extension candidates from the most recent node
    if (chain.nodes.length > 0) {
      const ni = chain.nodes.length - 1;
      const sh = STATE.shadowsByChain[chain.id]?.[ni];
      if (sh) {
        const ext = document.createElement("div");
        ext.className = "chain-extend";

        const fwd = candidatesForExtend(sh.forward, "forward", chain);
        const bwd = candidatesForExtend(sh.backward, "backward", chain);

        // Use appendChild only — innerHTML += would re-parse and strip event listeners.
        if (fwd.length > 0) {
          const h = document.createElement("h4");
          h.textContent = "↓ extend forward";
          ext.appendChild(h);
          const list = document.createElement("div");
          list.className = "candidates";
          for (const c of fwd) list.appendChild(makeCandRow(c, chain));
          ext.appendChild(list);
        }
        if (bwd.length > 0) {
          const h = document.createElement("h4");
          h.textContent = "↑ ancestors";
          ext.appendChild(h);
          const list = document.createElement("div");
          list.className = "candidates";
          for (const c of bwd) list.appendChild(makeCandRow(c, chain));
          ext.appendChild(list);
        }
        card.appendChild(ext);
      }
    }

    panel.appendChild(card);
  }
}

function candidatesForExtend(arr, dir, chain) {
  // Show 1 next-layer candidate per layer for the next 4 layers (forward)
  // or previous 4 layers (backward), capped at 8 total.
  const head = arr.slice(0, 12);
  // Drop ones already in the chain
  const seen = new Set(chain.nodes.map((n) => `${n.layer}/${n.channel}/${n.slot}`));
  return head.filter((c) => !seen.has(`${c.layer}/${c.channel}/${c.slot}`)).slice(0, 8);
}

function makeCandRow(c, chain) {
  const tokens = topTokensForConcept(c.layer, c.channel, c.slot, 3);
  const chLetter = CHANNEL_LETTER[c.channel];
  const row = document.createElement("div");
  row.className = "cand";
  row.innerHTML = `
    <div class="score">${c.score >= 0 ? "+" : "−"}${Math.abs(c.score).toFixed(2)}</div>
    <div class="id">L${c.layer.toString().padStart(2, "0")} <span class="ch ${chLetter}" style="display:inline-block;padding:0 4px;background:#222;border-radius:2px;font-size:9px;">${chLetter}</span> i=${c.slot.toString().padStart(2, "0")}</div>
    <div class="toks">${tokens.map((t) => escapeHtml(t.label)).join(" · ")}</div>
  `;
  row.addEventListener("click", (e) => {
    e.stopPropagation();
    STATE.activeChainId = chain.id;
    if (c.channel !== STATE.channel) {
      STATE.channel = c.channel;
      document.getElementById("channel-sel").value = c.channel;
    }
    pinConcept(c.layer, c.channel, c.slot);
  });
  return row;
}

