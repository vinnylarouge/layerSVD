function downloadMotifs() {
  const out = {
    exemplar_id: STATE.exemplarId,
    channels: CHANNELS,
    chains: STATE.chains.map((c) => ({
      id: c.id,
      label: c.label,
      color: c.color,
      nodes: c.nodes.map((n, i) => {
        const tokens = topTokensForConcept(n.layer, n.channel, n.slot, 6);
        let edge_to_parent = null;
        if (i > 0) {
          const p = c.nodes[i - 1];
          const a = activationSeries(p.layer, p.channel, p.slot);
          const b = activationSeries(n.layer, n.channel, n.slot);
          if (a && b) edge_to_parent = +cosSim(a, b).toFixed(4);
        }
        return {
          layer: n.layer, channel: n.channel, slot: n.slot,
          edge_to_parent,
          top_tokens: tokens.map((t) => ({
            token: t.label,
            activation: +t.activation.toFixed(3),
          })),
        };
      }),
    })),
  };
  const blob = new Blob([JSON.stringify(out, null, 2)], { type: "application/json" });
  const url = URL.createObjectURL(blob);
  const a = document.createElement("a");
  a.href = url;
  a.download = `${STATE.exemplarId}-motifs.json`;
  a.click();
  URL.revokeObjectURL(url);
}
