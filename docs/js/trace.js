// Concept chain miner
// =====================================================================
//
// Substrate: a (layer × concept slot) heatmap of one channel's
// activations at the current token. Cells are clickable.
//
// Discovery loop:
//   - Click a cell      → pin a concept onto the active chain
//   - The system computes cosine-similarity flows across all five
//     channels at every other layer ("shadows")
//   - Same-channel shadows render as colored rings on the heatmap
//   - All shadows (cross-channel included) surface as ranked
//     candidates in the chain panel — click one to extend the chain
//   - Multiple chains can run in parallel under different colors
//   - "download motifs.json" exports each chain with its labels
//     and the top firing tokens per node
//
// Channels:
//   acts_q, acts_k, acts_v, acts_down_up : projection of layer L's
//     residual onto the right singular vectors of layer L+1's
//     wq / wk / wv / composed (W_down·W_up). "What L+1 listens for".
//   speak : projection of layer L's residual onto the LEFT singular
//     vectors of layer L's own composed (W_down·W_up).
//     "What L's FFN spoke this token."

// ---------------------------------------------------------------------
// Constants & state
// ---------------------------------------------------------------------

const CHANNELS = ["acts_q", "acts_k", "acts_v", "acts_down_up", "speak"];
const CHANNEL_LETTER = {
  acts_q: "q", acts_k: "k", acts_v: "v",
  acts_down_up: "d", speak: "s",
};
const CHAIN_COLORS = [
  "#ff6f6f", "#57c98a", "#ffcb47", "#b87cff",
  "#6cdfff", "#f5876f", "#76e0a3", "#e3a4ff",
];

const STATE = {
  index: null,
  exemplarId: null,
  trace: null,
  channel: "acts_down_up",
  tokenIdx: 0,
  // Chain mining state
  chains: [],          // [{id, color, label, nodes:[{layer,channel,slot}]}]
  shadowsByChain: {},  // {chainId: [{forward:[...], backward:[...]} per node index]}
  activeChainId: null,
  // Geometry cache (set on render)
  cellW: 0, cellH: 0, padL: 0, padT: 0,
};

const SVG_NS = "http://www.w3.org/2000/svg";
function el(tag, attrs = {}, parent = null) {
  const node = document.createElementNS(SVG_NS, tag);
  for (const [k, v] of Object.entries(attrs)) {
    if (v !== undefined && v !== null) node.setAttribute(k, String(v));
  }
  if (parent) parent.appendChild(node);
  return node;
}

function escapeHtml(s) {
  return String(s)
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;");
}

