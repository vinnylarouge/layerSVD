// ---------------------------------------------------------------------
// Decoding cached per (layer, channel)
// ---------------------------------------------------------------------

const DECODE_CACHE = new Map();
function decodeLayerChannel(layer, channel) {
  const key = `${layer}/${channel}`;
  if (DECODE_CACHE.has(key)) return DECODE_CACHE.get(key);
  const layerObj = STATE.trace.layers[String(layer)];
  if (!layerObj) return null;
  const ent = layerObj[channel];
  if (!ent) return null;
  const bin = atob(ent.data);
  const buf = new Uint8Array(bin.length);
  for (let i = 0; i < bin.length; i++) buf[i] = bin.charCodeAt(i);
  const u16 = new Uint16Array(buf.buffer, buf.byteOffset, buf.byteLength / 2);
  const f32 = fp16ToFp32(u16);
  const out = { K: ent.shape[0], n_tokens: ent.shape[1], data: f32 };
  DECODE_CACHE.set(key, out);
  return out;
}

function activationSeries(L, channel, slot) {
  const dec = decodeLayerChannel(L, channel);
  if (!dec) return null;
  const out = new Float32Array(dec.n_tokens);
  for (let t = 0; t < dec.n_tokens; t++) out[t] = dec.data[slot * dec.n_tokens + t];
  return out;
}

function topTokensForConcept(L, channel, slot, n) {
  const series = activationSeries(L, channel, slot);
  if (!series) return [];
  const idxs = Array.from({ length: series.length }, (_, i) => i);
  idxs.sort((a, b) => Math.abs(series[b]) - Math.abs(series[a]));
  return idxs.slice(0, n).map((i) => ({
    label: STATE.trace.tokens[i],
    activation: series[i],
    tokenIdx: i,
  }));
}

// ---------------------------------------------------------------------
// Cosine similarity & shadow computation
// ---------------------------------------------------------------------

function cosSim(a, b) {
  const n = Math.min(a.length, b.length);
  let dot = 0, na = 0, nb = 0;
  for (let i = 0; i < n; i++) {
    dot += a[i] * b[i];
    na += a[i] * a[i];
    nb += b[i] * b[i];
  }
  const denom = Math.sqrt(na * nb) + 1e-12;
  return dot / denom;
}

// For one pinned node, compute its top shadows at every other layer.
// Returns {forward: [...], backward: [...]} where each entry is
// {layer, channel, slot, score} sorted by |score| desc within its layer.
function computeShadows(node, opts = {}) {
  const topPerLayer = opts.topPerLayer || 3;
  const minAbs = opts.minAbs || 0.10;
  const nL = STATE.trace.n_layer;
  const pinSeries = activationSeries(node.layer, node.channel, node.slot);
  if (!pinSeries) return { forward: [], backward: [] };

  function flowsAtLayer(L) {
    const cands = [];
    for (const ch of CHANNELS) {
      const dec = decodeLayerChannel(L, ch);
      if (!dec) continue;
      for (let j = 0; j < dec.K; j++) {
        // skip identity (same node)
        if (L === node.layer && ch === node.channel && j === node.slot) continue;
        const cand = new Float32Array(dec.n_tokens);
        for (let t = 0; t < dec.n_tokens; t++) cand[t] = dec.data[j * dec.n_tokens + t];
        const score = cosSim(pinSeries, cand);
        if (Math.abs(score) >= minAbs) {
          cands.push({ layer: L, channel: ch, slot: j, score });
        }
      }
    }
    cands.sort((a, b) => Math.abs(b.score) - Math.abs(a.score));
    return cands.slice(0, topPerLayer);
  }

  const forward = [];
  for (let L = node.layer + 1; L < nL; L++) forward.push(...flowsAtLayer(L));
  const backward = [];
  for (let L = node.layer - 1; L >= 0; L--) backward.push(...flowsAtLayer(L));
  return { forward, backward };
}

