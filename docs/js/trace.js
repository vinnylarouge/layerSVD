const HM_W = 820;
const HM_H = 720;

function renderHeatmap() {
  if (!STATE.trace) return;
  const svg = document.getElementById("trace-heatmap");
  while (svg.firstChild) svg.removeChild(svg.firstChild);

  const nL = STATE.trace.n_layer;
  const dec0 = decodeLayerChannel(0, STATE.channel);
  if (!dec0) {
    el("text", { x: 12, y: 24, fill: "#8a96aa", "font-family": "ui-monospace,monospace", "font-size": 12 }, svg)
      .textContent = `channel "${STATE.channel}" not present in this trace`;
    svg.setAttribute("viewBox", `0 0 ${HM_W} 60`);
    return;
  }
  const K = dec0.K;

  svg.setAttribute("viewBox", `0 0 ${HM_W} ${HM_H}`);
  svg.style.aspectRatio = `${HM_W} / ${HM_H}`;

  const PAD_L = 38, PAD_T = 18, PAD_R = 8, PAD_B = 8;
  const cellW = (HM_W - PAD_L - PAD_R) / K;
  const cellH = (HM_H - PAD_T - PAD_B) / nL;
  STATE.cellW = cellW;
  STATE.cellH = cellH;
  STATE.padL = PAD_L;
  STATE.padT = PAD_T;

  // Reusable arrowhead marker that picks up the line's stroke colour.
  const defs = el("defs", {}, svg);
  const marker = el("marker", {
    id: "chain-arrow",
    viewBox: "0 0 8 8",
    refX: "6.4",
    refY: "4",
    markerWidth: "5",
    markerHeight: "5",
    orient: "auto",
    markerUnits: "userSpaceOnUse",
  }, defs);
  el("path", { d: "M0,0 L8,4 L0,8 Z", fill: "context-stroke" }, marker);

  // Compute row activations for current token + max abs for normalization
  const t = STATE.tokenIdx;
  let maxAbs = 1e-12;
  const grid = new Array(nL);
  for (let L = 0; L < nL; L++) {
    const dec = decodeLayerChannel(L, STATE.channel);
    if (!dec) { grid[L] = null; continue; }
    const row = new Float32Array(K);
    for (let i = 0; i < K; i++) {
      const v = dec.data[i * dec.n_tokens + t];
      row[i] = v;
      if (Math.abs(v) > maxAbs) maxAbs = Math.abs(v);
    }
    grid[L] = row;
  }

  // Cells
  for (let L = 0; L < nL; L++) {
    const row = grid[L];
    if (!row) continue;
    for (let i = 0; i < K; i++) {
      const v = Math.max(-1, Math.min(1, row[i] / maxAbs));
      let r, g, b;
      if (v >= 0) { r = 255 * v; g = 160 * v; b = 80 * v; }
      else { r = 80 * -v; g = 160 * -v; b = 255 * -v; }
      el("rect", {
        x: PAD_L + i * cellW,
        y: PAD_T + L * cellH,
        width: cellW + 0.6,
        height: cellH + 0.6,
        fill: `rgb(${r | 0},${g | 0},${b | 0})`,
        class: "tcell",
        "data-l": String(L),
        "data-i": String(i),
      }, svg);
    }
  }

  // Layer labels every 5 rows
  for (let L = 0; L < nL; L += 5) {
    el("text", {
      x: 4, y: PAD_T + L * cellH + cellH * 0.6 + 1,
      fill: "#8a96aa",
      "font-family": "ui-monospace,monospace",
      "font-size": 9.5,
    }, svg).textContent = `L${L.toString().padStart(2, "0")}`;
  }

  // Cell geometry helper used by arrow drawing
  const cellCenter = (L, slot) => [
    PAD_L + slot * cellW + cellW / 2,
    PAD_T + L * cellH + cellH / 2,
  ];

  // Chain arrows: connect consecutive nodes in the order they were pinned.
  // Drawn beneath pin/shadow markers so the markers stay on top.
  for (const chain of STATE.chains) {
    if (chain.nodes.length < 2) continue;
    for (let i = 1; i < chain.nodes.length; i++) {
      const a = chain.nodes[i - 1];
      const b = chain.nodes[i];
      if (a.layer === b.layer && a.slot === b.slot) continue;
      const [x1, y1] = cellCenter(a.layer, a.slot);
      const [x2, y2] = cellCenter(b.layer, b.slot);
      // Shorten the line at both ends so the arrow doesn't bury the pin marker
      const dx = x2 - x1, dy = y2 - y1;
      const dist = Math.hypot(dx, dy);
      if (dist < 0.5) continue;
      const trim = Math.min(cellW * 0.45, dist * 0.4);
      const ux = dx / dist, uy = dy / dist;
      const sx = x1 + ux * trim;
      const sy = y1 + uy * trim;
      const ex = x2 - ux * trim;
      const ey = y2 - uy * trim;
      el("line", {
        x1: sx, y1: sy, x2: ex, y2: ey,
        stroke: chain.color,
        "stroke-width": 1.2,
        opacity: 0.55,
        "marker-end": "url(#chain-arrow)",
        "pointer-events": "none",
      }, svg);
    }
  }

  // Pin and shadow overlays
  for (const chain of STATE.chains) {
    const shadows = STATE.shadowsByChain[chain.id] || [];

    // Shadows first (so pins draw on top)
    for (let ni = 0; ni < chain.nodes.length; ni++) {
      const sh = shadows[ni];
      if (!sh) continue;
      for (const dir of ["forward", "backward"]) {
        for (const s of sh[dir]) {
          if (s.channel !== STATE.channel) continue;
          const cx = PAD_L + s.slot * cellW + cellW / 2;
          const cy = PAD_T + s.layer * cellH + cellH / 2;
          const r = Math.max(2.5, Math.min(cellH, cellW) * 0.42 * Math.abs(s.score));
          el("circle", {
            cx, cy, r,
            fill: "none",
            stroke: chain.color,
            "stroke-width": s.score >= 0 ? 1.4 : 1.4,
            "stroke-dasharray": s.score >= 0 ? null : "1.5 1.5",
            opacity: 0.85,
            class: "shadow-marker",
            "data-l": String(s.layer),
            "data-c": s.channel,
            "data-i": String(s.slot),
            "data-chain": chain.id,
          }, svg);
        }
      }
    }

    // Pinned cells: thick ring on the chain color
    for (let ni = 0; ni < chain.nodes.length; ni++) {
      const n = chain.nodes[ni];
      if (n.channel !== STATE.channel) {
        // Off-channel pin: small marker at the cell with channel letter
        const x = PAD_L + n.slot * cellW;
        const y = PAD_T + n.layer * cellH;
        el("circle", {
          cx: x + 4, cy: y + 4, r: 2.6,
          fill: chain.color,
          class: "off-channel-pin",
          "pointer-events": "none",
        }, svg);
        el("text", {
          x: x + 8, y: y + 8,
          fill: chain.color,
          "font-family": "ui-monospace,monospace",
          "font-size": 8,
          "font-weight": 700,
          "pointer-events": "none",
        }, svg).textContent = CHANNEL_LETTER[n.channel];
        continue;
      }
      el("rect", {
        x: PAD_L + n.slot * cellW + 0.5,
        y: PAD_T + n.layer * cellH + 0.5,
        width: cellW - 0.5,
        height: cellH - 0.5,
        fill: "none",
        stroke: chain.color,
        "stroke-width": 2.4,
        class: "pin-marker",
        "pointer-events": "none",
      }, svg);
    }
  }

  // Click handler
  svg.onclick = (evt) => {
    const tg = evt.target;
    if (tg.classList.contains("shadow-marker")) {
      const L = parseInt(tg.dataset.l, 10);
      const ch = tg.dataset.c;
      const slot = parseInt(tg.dataset.i, 10);
      const cid = tg.dataset.chain;
      // Make that chain active and extend it
      STATE.activeChainId = cid;
      if (ch !== STATE.channel) {
        STATE.channel = ch;
        document.getElementById("channel-sel").value = ch;
      }
      pinConcept(L, ch, slot);
      return;
    }
    if (tg.classList.contains("tcell")) {
      const L = parseInt(tg.dataset.l, 10);
      const slot = parseInt(tg.dataset.i, 10);
      pinConcept(L, STATE.channel, slot);
      return;
    }
  };
}

