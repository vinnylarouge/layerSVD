function newChain() {
  const id = "c" + Math.random().toString(36).slice(2, 8);
  const color = CHAIN_COLORS[STATE.chains.length % CHAIN_COLORS.length];
  STATE.chains.push({ id, color, label: "", nodes: [] });
  STATE.activeChainId = id;
  return id;
}

function findChain(id) {
  return STATE.chains.find((c) => c.id === id);
}

function activeChain() {
  if (!STATE.activeChainId) return null;
  return findChain(STATE.activeChainId);
}

function recomputeShadows(chain) {
  STATE.shadowsByChain[chain.id] = chain.nodes.map((n) => computeShadows(n));
}

function pinConcept(L, channel, slot) {
  // Auto-create a chain on first pin
  if (!activeChain()) newChain();
  const chain = activeChain();
  // Toggle: if already pinned in this chain, remove (deselect)
  const existingIdx = chain.nodes.findIndex(
    (n) => n.layer === L && n.channel === channel && n.slot === slot
  );
  if (existingIdx >= 0) {
    chain.nodes.splice(existingIdx, 1);
  } else {
    chain.nodes.push({ layer: L, channel, slot });
  }
  recomputeShadows(chain);
  renderHeatmap();
  renderChainsPanel();
}

function removeNodeAt(chainId, nodeIdx) {
  const chain = findChain(chainId);
  if (!chain) return;
  if (nodeIdx < 0 || nodeIdx >= chain.nodes.length) return;
  chain.nodes.splice(nodeIdx, 1);
  recomputeShadows(chain);
  renderHeatmap();
  renderChainsPanel();
}

function deleteChain(id) {
  const idx = STATE.chains.findIndex((c) => c.id === id);
  if (idx < 0) return;
  STATE.chains.splice(idx, 1);
  delete STATE.shadowsByChain[id];
  if (STATE.activeChainId === id) {
    STATE.activeChainId = STATE.chains[0]?.id || null;
  }
  renderHeatmap();
  renderChainsPanel();
}

function setActiveChain(id) {
  STATE.activeChainId = id;
  renderChainsPanel();
}

