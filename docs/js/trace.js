// Token tracer view.
//
// Data flow:
//   1. Load docs/data/traces/index.json -> list of exemplars
//   2. On exemplar select, load docs/data/traces/<id>.json (compact)
//   3. Decode base64-fp16 layer arrays for the selected channel
//   4. Render: token list, per-layer concept heatmap, label editor
//
// Labels: stored in localStorage keyed by `${exemplarId}::${layer}::${concept}`.
// "download labels.json" exports them.

const STATE = {
  index: null,
  exemplarId: null,
  trace: null,
  channel: "acts_down_up",
  tokenIdx: 0,
  selectedLayer: null,
  selectedConcept: null,
};

(async function () {
  // Load the trace index
  let index;
  try {
    const r = await fetch("data/traces/index.json");
    if (!r.ok) throw new Error(r.status);
    index = await r.json();
  } catch (e) {
    document.querySelector(".trace-grid").innerHTML =
      `<div class="trace-card" style="grid-column:1/-1;">
       <h2>no traces yet</h2>
       <p style="color:var(--text-dim);font-size:12px;">
         Captured-activation traces are generated by
         <code>tools/eval-capture</code> + <code>layersvd.project</code>.
         Once they're committed under <code>docs/data/traces/</code>, this view
         will populate the dropdown above. The architecture map and SVD browser
         work standalone.
       </p>
       </div>`;
    return;
  }
  STATE.index = index;

  const sel = document.getElementById("exemplar-sel");
  for (const item of index.exemplars) {
    const opt = document.createElement("option");
    opt.value = item.id;
    opt.textContent = item.label;
    sel.appendChild(opt);
  }
  sel.addEventListener("change", () => loadExemplar(sel.value));

  document.getElementById("channel-sel").addEventListener("change", (e) => {
    STATE.channel = e.target.value;
    render();
  });

  document.getElementById("token-slider").addEventListener("input", (e) => {
    STATE.tokenIdx = parseInt(e.target.value, 10);
    updateCurrentTokenLabel();
    render();
  });

  document.getElementById("download-labels").addEventListener("click", downloadLabels);

  if (index.exemplars.length > 0) {
    sel.value = index.exemplars[0].id;
    await loadExemplar(index.exemplars[0].id);
  }
})().catch((err) => {
  console.error(err);
  document.body.insertAdjacentHTML(
    "afterbegin",
    `<pre style="color:#ff6f6f;padding:20px;">${err.stack || err.message}</pre>`
  );
});

async function loadExemplar(id) {
  STATE.exemplarId = id;
  const r = await fetch(`data/traces/${id}.json`);
  if (!r.ok) throw new Error("trace fetch failed: " + r.status);
  STATE.trace = await r.json();

  const slider = document.getElementById("token-slider");
  slider.max = String(STATE.trace.n_tokens - 1);
  slider.value = "0";
  STATE.tokenIdx = 0;

  // Token list
  const list = document.getElementById("token-list");
  list.innerHTML = "";
  for (let i = 0; i < STATE.trace.n_tokens; i++) {
    const tk = document.createElement("div");
    tk.className = "tk";
    tk.dataset.idx = String(i);
    tk.textContent = `${i.toString().padStart(3)}  ${STATE.trace.tokens[i]}`;
    tk.addEventListener("click", () => {
      STATE.tokenIdx = i;
      slider.value = String(i);
      updateCurrentTokenLabel();
      render();
    });
    list.appendChild(tk);
  }

  updateCurrentTokenLabel();
  render();
}

function updateCurrentTokenLabel() {
  const el = document.getElementById("current-token");
  if (!STATE.trace) { el.textContent = "—"; return; }
  el.textContent = `[${STATE.tokenIdx}] ${STATE.trace.tokens[STATE.tokenIdx]}`;
  document.querySelectorAll("#token-list .tk").forEach((tk) => {
    tk.classList.toggle("active", parseInt(tk.dataset.idx, 10) === STATE.tokenIdx);
  });
}

// Cache decoded arrays per (layer, channel) so re-rendering is fast.
const DECODE_CACHE = new Map();

function decodeLayerChannel(layer, channel) {
  const key = `${layer}/${channel}`;
  if (DECODE_CACHE.has(key)) return DECODE_CACHE.get(key);
  const layerObj = STATE.trace.layers[String(layer)];
  if (!layerObj) return null;
  const ent = layerObj[channel];
  if (!ent) return null;
  const bin = atob(ent.data);
  const buf = new Uint8Array(bin.length);
  for (let i = 0; i < bin.length; i++) buf[i] = bin.charCodeAt(i);
  const u16 = new Uint16Array(buf.buffer, buf.byteOffset, buf.byteLength / 2);
  const f32 = fp16ToFp32(u16);
  // Reshape: ent.shape = [K, n_tokens]
  const out = { K: ent.shape[0], n_tokens: ent.shape[1], data: f32 };
  DECODE_CACHE.set(key, out);
  return out;
}

function render() {
  if (!STATE.trace) return;
  const c = document.getElementById("trace-heatmap");
  const ctx = c.getContext("2d");
  const W = c.width, H = c.height;
  ctx.clearRect(0, 0, W, H);

  const nLayer = STATE.trace.n_layer;
  // Determine K of selected channel by checking layer 0
  const sample = decodeLayerChannel(0, STATE.channel);
  if (!sample) {
    ctx.fillStyle = "#8a96aa";
    ctx.font = "12px ui-monospace,Menlo,monospace";
    ctx.fillText(`channel "${STATE.channel}" not present in this trace`, 12, 24);
    return;
  }
  const K = sample.K;
  const cellW = W / K;
  const cellH = H / nLayer;

  // Compute global max for normalization across layers/concepts at this token
  const t = STATE.tokenIdx;
  let maxAbs = 1e-12;
  const perLayer = new Array(nLayer);
  for (let L = 0; L < nLayer; L++) {
    const dec = decodeLayerChannel(L, STATE.channel);
    if (!dec) { perLayer[L] = null; continue; }
    const row = new Float32Array(K);
    for (let i = 0; i < K; i++) {
      const v = dec.data[i * dec.n_tokens + t];
      row[i] = v;
      if (Math.abs(v) > maxAbs) maxAbs = Math.abs(v);
    }
    perLayer[L] = row;
  }

  // Draw heatmap
  for (let L = 0; L < nLayer; L++) {
    const row = perLayer[L];
    if (!row) continue;
    for (let i = 0; i < K; i++) {
      const v = row[i] / maxAbs;
      const a = Math.max(-1, Math.min(1, v));
      // diverging colormap
      let r, g, b;
      if (a >= 0) {
        r = Math.floor(255 * a); g = Math.floor(160 * a); b = Math.floor(80 * a);
      } else {
        r = Math.floor(80 * -a); g = Math.floor(160 * -a); b = Math.floor(255 * -a);
      }
      ctx.fillStyle = `rgb(${r},${g},${b})`;
      ctx.fillRect(i * cellW, L * cellH, Math.ceil(cellW), Math.ceil(cellH));
    }
  }

  // Click handler — choose layer/concept for labelling
  c.onclick = (e) => {
    const rect = c.getBoundingClientRect();
    const x = (e.clientX - rect.left) * (c.width / rect.width);
    const y = (e.clientY - rect.top) * (c.height / rect.height);
    const conceptIdx = Math.floor(x / cellW);
    const layerIdx = Math.floor(y / cellH);
    if (layerIdx < 0 || layerIdx >= nLayer || conceptIdx < 0 || conceptIdx >= K) return;
    STATE.selectedLayer = layerIdx;
    STATE.selectedConcept = conceptIdx;
    refreshLabelsPanel();
  };
  refreshLabelsPanel();
}

function labelKey(L, i) {
  return `${STATE.exemplarId}::${STATE.channel}::L${L}::C${i}`;
}

function refreshLabelsPanel() {
  const ctxEl = document.getElementById("label-context");
  const list = document.getElementById("label-list");
  if (STATE.selectedLayer === null) {
    ctxEl.textContent = "click a heatmap cell to label";
    list.innerHTML = "";
    return;
  }
  const L = STATE.selectedLayer;
  const i = STATE.selectedConcept;
  ctxEl.innerHTML = `<strong>L=${L} concept=${i}</strong> · channel <code>${STATE.channel}</code>`;
  list.innerHTML = "";
  const key = labelKey(L, i);
  const cur = localStorage.getItem(key) || "";
  const input = document.createElement("input");
  input.placeholder = "label this concept (e.g. 'token type', 'punctuation', 'subject noun')";
  input.value = cur;
  input.addEventListener("input", (e) => localStorage.setItem(key, e.target.value));
  list.appendChild(input);

  // Show neighbors at this layer for context
  const note = document.createElement("div");
  note.style.color = "var(--text-dim)";
  note.style.fontSize = "10px";
  note.style.marginTop = "8px";
  note.textContent = "tip: also try labelling concepts at adjacent layers — coherent labels mean stable concepts.";
  list.appendChild(note);
}

function downloadLabels() {
  const out = {};
  for (let i = 0; i < localStorage.length; i++) {
    const k = localStorage.key(i);
    if (!k) continue;
    if (!k.includes("::")) continue;
    out[k] = localStorage.getItem(k);
  }
  const blob = new Blob([JSON.stringify(out, null, 2)], { type: "application/json" });
  const url = URL.createObjectURL(blob);
  const a = document.createElement("a");
  a.href = url;
  a.download = "layersvd-labels.json";
  a.click();
  URL.revokeObjectURL(url);
}
